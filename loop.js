// For loop
for (let i = 0; i < 10; i++) {
  console.log("hi");
}

const Fname = ["Bharat", "naveen", "Raghavendra"];
for (let i = 0; i < Fname.length; i++) {
  console.log(Fname[i]);
}

// While Loop
let i = 0;
while (i < 10) {
  console.log(i);
  i++;
}

// do while

let j = 0;
do {
  j++;
  console.log(j);
} while (j < 20);
